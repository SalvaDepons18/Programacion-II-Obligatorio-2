    // Metodo para cargar la lista de tematicas elegidas
    public void cargarListaElegidas(){
        ArrayList<Tematica> Tematicas = this.temas;
        Tematica[] tematicasArray = new Tematica[Tematicas.size()];

        for (int i = 0; i < Tematicas.size(); i++) {
             Tematica tem = Tematicas.get(i);
             tematicasArray[i] = tem;
        }

        ListaTematicasElegidas.setListData(tematicasArray);        
    }
    
    // Metodo para cargar la lista de las tematicas 
    public void cargarLista(){
        ArrayList<Tematica> tematicas = modelo.getListaTematicas();
        Tematica[] TematicasArray = new Tematica[tematicas.size()];

        for(int i = 0; i < tematicas.size(); i++){
            Tematica tem = tematicas.get(i);
            TematicasArray[i] = tem;
        }

        listListaTemasRequeridosPuesto.setListData(TematicasArray);
    }
    
