    // Funcionamiento del boton registro
    private void btnRegistrarPuestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarPuestoActionPerformed

        // Tomo los datos ingresados
        String nom = txtNombrePuestoRegistro.getText();
        String Formato = "";
        if (rbtnRemotoPuesto.isSelected()) {
            Formato = "remoto";
        } else if (rbtnMixtoPuesto.isSelected()) {
            Formato = "mixto";
        } else if (rbtnPresencialPuesto.isSelected()) {
            Formato = "presencial";
        }
        // Verifico que los campos tengan datos
        if(nom.equals("") || Formato.equals("")){
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos.");
        // Verifico que tenga tematicas ingresada
        }else if(temas.size() == 0){
            JOptionPane.showMessageDialog(this, "Debe agregar al menos una tematica.");
        // Verifico que el nombre puesto no exista
        }else if(modelo.verificarPuestoUnicoPorNombre(nom)){
            JOptionPane.showMessageDialog(this, "El nombre del puesto ya existe.");
        }else{
            // Creo un puesto con los datos ingresado y lo agrego a la lista
            Puesto p = new Puesto(nom, Formato, temas);
            modelo.agregarPuesto(p);
            txtNombrePuestoRegistro.setText("");
            rbtnRemotoPuesto.setSelected(true);
            temas = new ArrayList<>();
            cargarListaElegidas();
        }
    }//GEN-LAST:event_btnRegistrarPuestoActionPerformed

