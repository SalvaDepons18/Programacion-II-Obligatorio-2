    // Funcionamiento del boton agregar tematica
    private void btnAgregarTemaPuestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarTemaPuestoActionPerformed
        
        // Tomo la tematica seleccionada
        Tematica temax = listListaTemasRequeridosPuesto.getSelectedValue();
        
        // Verifico que el tema no este seleccionado y cargo la lista con el nuevo tema 
        if (temas.contains(temax)){
           JOptionPane.showMessageDialog(this, "Tema ya elegido.");
        }else if(listListaTemasRequeridosPuesto.isSelectionEmpty()){
           JOptionPane.showMessageDialog(this, "No hay ninguna tematica seleccionada.");
        }else{
           temas.add(temax);
           cargarListaElegidas();
        }      
        
     
    }//GEN-LAST:event_btnAgregarTemaPuestoActionPerformed

    // Funcionamiento del boton eliminar tematica
    private void btnEliminarTemaPuestoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTemaPuestoActionPerformed
        
        // Tomo la tematica seleccioanda
        Tematica temax = ListaTematicasElegidas.getSelectedValue();
        try{
            // Si esta en la lista el seleccionado lo elimino 
            if (temas.contains(temax)){
                temas.remove(temax);
                cargarListaElegidas();  
            // Verifico que la lista tenga elementos 
            }else if(temas.size() == 0){
                JOptionPane.showMessageDialog(this, "No existen temas agregados."); 
            }else{
                JOptionPane.showMessageDialog(this, "El tema seleccionado no esta agregado."); 
            }
        // Capturo Expetion
        }catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnEliminarTemaPuestoActionPerformed

