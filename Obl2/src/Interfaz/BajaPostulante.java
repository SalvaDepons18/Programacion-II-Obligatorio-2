    // Funcion que toma la lista de postulantes y la carga al JList
    public void cargarLista(){
        ArrayList<Postulante> posts = modelo.getListaPostulantes();
        Postulante[] PostultantesArray = new Postulante[posts.size()];

        for (int i = 0; i < posts.size(); i++) {
            PostultantesArray[i] = posts.get(i);
        }

        lstListaDePostulantes.setListData(PostultantesArray);
    
    }
