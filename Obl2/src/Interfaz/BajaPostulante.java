    // Funcionamiento del boton de eliminar postulante
    private void btnEliminarBajaPostulantesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarBajaPostulantesActionPerformed

        try{
            // Valido que este seleccionado algo 
            if(lstListaDePostulantes.getSelectedValue() != null){
                // Tomo el postulante y lo elimino junto a sus entrevistas
                Postulante p = lstListaDePostulantes.getSelectedValue();
                ArrayList<Entrevista> entrevistasP = modelo.entrevistasDePostulante(p);
                for (Entrevista e: entrevistasP){
                    modelo.quitarEntrevista(e);   
                }
                modelo.quitarPostulante(p);
                cargarLista();   
            }else{
            JOptionPane.showMessageDialog(this, "No se ha seleccionado ningún elemento para eliminar.");
            }
        // Capturo Exeption 
        }catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnEliminarBajaPostulantesActionPerformed

