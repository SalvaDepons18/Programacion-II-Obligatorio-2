    private void btnRegistrarIngresoEntrevistaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarIngresoEntrevistaActionPerformed
        
        // Toma los datos seleccioandos
        Postulante p = listPostulantesIngresoEntrevista.getSelectedValue();
        Evaluador e = listEvaluadoresIngresoEntrevista.getSelectedValue();
        String com = listComentarioIngresoEntrevista.getText();
        String puntaje = (String)comboPuntajeIngresoEntrevista.getSelectedItem();
        String numero = "";
        
        // Verifica que esten ambas listas seleccionadas
        if (listEvaluadoresIngresoEntrevista.isSelectionEmpty() || listPostulantesIngresoEntrevista.isSelectionEmpty()){
            JOptionPane.showMessageDialog(this, "Debe Seleccionar Postulante y Evaluador.");
          // Verifica que llene todos los datos
        } else if (listComentarioIngresoEntrevista.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe completar todos los campos.");
        } else{
            // Creo una nueva entrevista con los datos y la agrego a la lista, ademas incremento el contador de entervistas
            modelo.aumentarCantDeEntrevistas();
            numero = Integer.toString(modelo.getCantEntrevistas());
            Entrevista entre = new Entrevista(e, p, com, puntaje, numero);
            JOptionPane.showMessageDialog(this, "Su numero de entrevista es: " + numero + ".");
            modelo.agregarEntrevista(entre);
            listComentarioIngresoEntrevista.setText("");
            comboPuntajeIngresoEntrevista.setSelectedIndex(0);
            listEvaluadoresIngresoEntrevista.clearSelection();
            listPostulantesIngresoEntrevista.clearSelection();//hay que hacer que no se cierre? preguntarle a la profe igual
        }

    }//GEN-LAST:event_btnRegistrarIngresoEntrevistaActionPerformed

