    // Metodo que carga la lista de evaluadores
    public void cargarListaEvauadores(){
        ArrayList<Evaluador> evaluadores = modelo.getListaEvaluadores();
        Evaluador[] EvaluadoresArray = new Evaluador[evaluadores.size()];

        for(int i = 0; i < evaluadores.size(); i++){
            Evaluador eva = evaluadores.get(i);
            EvaluadoresArray[i] = eva;
        }
        
        listEvaluadoresIngresoEntrevista.setListData(EvaluadoresArray);
    }
    
    // Metodo que carga la lista de postulantes
    public void cargarListaPostulantes(){
        ArrayList<Postulante> postulantes = modelo.getListaPostulantes();
        Postulante[] PostulantesArray = new Postulante[postulantes.size()];

        for(int i = 0; i < postulantes.size(); i++){
            Postulante pos = postulantes.get(i);
            PostulantesArray[i] = pos;
        }

        listPostulantesIngresoEntrevista.setListData(PostulantesArray);
    }
    
