    // Metodo encargado de recuperar el sistema previo
    public static Sistema cargarSistema(){
        Sistema resultado = null;
        try{
            // Le digo que intente cargar los datos del archivo "Sistema.ser" y creo un nuevo sistema con los datos
            ObjectInputStream in = new ObjectInputStream(Files.newInputStream(Paths.get("Sistema.ser")));
            Sistema nuevoSis = (Sistema) in.readObject();
            resultado = nuevoSis;
            in.close();
        // Capturo Exeption
        }catch(IOException | ClassNotFoundException e){
            System.out.println("Error al cargar");
        }
        return resultado;
    }
    
